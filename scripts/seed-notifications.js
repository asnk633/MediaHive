
const fs = require('fs');
const path = require('path');
const admin = require('firebase-admin');

// 1. Load Env
const envPath = path.resolve(__dirname, '../.env.local');
if (fs.existsSync(envPath)) {
    const envConfig = fs.readFileSync(envPath, 'utf8');
    envConfig.split('\n').forEach(line => {
        const parts = line.split('=');
        if (parts.length >= 2 && !line.startsWith('#')) {
            const key = parts[0].trim();
            let val = parts.slice(1).join('=').trim();
            if (val.startsWith('"') && val.endsWith('"')) val = val.slice(1, -1);
            process.env[key] = val;
        }
    });
}

// Set Emulator Host if not already set (for direct script runs)
// Check both common emulator ports
if (!process.env.FIRESTORE_EMULATOR_HOST) {
    process.env.FIRESTORE_EMULATOR_HOST = '127.0.0.1:8080';
    process.env.FIREBASE_AUTH_EMULATOR_HOST = '127.0.0.1:9099';
    console.log('Using default emulator hosts (no env set):', process.env.FIRESTORE_EMULATOR_HOST);
}

async function main() {
    const projectId = process.env.FIREBASE_ADMIN_PROJECT_ID || process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID || 'demo-thaiba';

    // In emulator mode, auth details matter less, but we initialize properly
    if (!admin.apps.length) {
        admin.initializeApp({
            projectId
        });
    }

    const email = process.argv[2] || 'verifier@thaiba.com'; // Allow passing email
    const db = admin.firestore();

    try {
        // Get User UID
        let userId;
        try {
            const userRecord = await admin.auth().getUserByEmail(email);
            userId = userRecord.uid;
            console.log(`Using UID: ${userId} (${email})`);
        } catch (e) {
            console.error(`User ${email} not found. Creating temporary user...`);
            // Create if missing (emulator convenience)
            const userRecord = await admin.auth().createUser({ email, password: 'password123' });
            userId = userRecord.uid;
            console.log(`Created test user: ${userId}`);
        }

        // Generate Notifications
        const priorities = ['high', 'medium', 'low'];
        const types = ['task_assigned', 'task_overdue', 'due_reminder', 'comment_mention', 'system_alert'];

        console.log('Clearing existing notifications for user...');
        const existing = await db.collection('notifications').where('userId', '==', userId).get();
        const batchSize = 500;
        let batch = db.batch();
        let count = 0;

        existing.docs.forEach((doc) => {
            batch.delete(doc.ref);
            count++;
            if (count >= batchSize) {
                // Creating new batch after commit is complex in sync loop, simplifying for script
                // Actually existing is likely small or we just append deletes. 
                // For now, let's just delete the first 500 found to keep it simple.
            }
        });
        if (count > 0) await batch.commit();
        console.log(`Deleted ${count} old notifications.`);

        console.log('Seeding 120 new notifications...');
        batch = db.batch();
        count = 0;

        const now = new Date();

        for (let i = 0; i < 120; i++) {
            const priority = priorities[i % 3]; // Equal split
            const type = types[i % 5];
            const isRead = i % 5 === 0; // 20% read
            const isArchived = i % 10 === 0; // 10% archived

            // Dates: 
            // 0-30: Today
            // 31-60: Yesterday
            // 61-90: Last 7 days
            // 91+: Older
            let date = new Date(now);
            if (i > 30 && i <= 60) date.setDate(now.getDate() - 1);
            else if (i > 60 && i <= 90) date.setDate(now.getDate() - 4);
            else if (i > 90) date.setDate(now.getDate() - 20);

            // Random jitter within the day
            date.setHours(Math.floor(Math.random() * 24), Math.floor(Math.random() * 60));

            const ref = db.collection('notifications').doc();
            batch.set(ref, {
                id: ref.id,
                userId: userId,
                type: type,
                title: `${priority.toUpperCase()} Priority: ${type} #${i}`,
                message: `This is a test notification generated by the seeding script. Index: ${i}. Priority: ${priority}.`,
                priority: priority,
                isRead: isRead,
                isArchived: isArchived,
                createdAt: admin.firestore.Timestamp.fromDate(date),
                updatedAt: admin.firestore.Timestamp.fromDate(date),
                actionUrl: '/tasks', // Generic link
                metadata: { taskId: `task-${i}` }
            });
            count++;
        }

        await batch.commit();
        console.log('Seeding complete! 120 notifications created.');
        console.log(`Login as ${email} to verify.`);

    } catch (err) {
        console.error('Error seeding notifications:', err);
    }
}

main().then(() => process.exit(0));
